    private void usernameKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_usernameKeyTyped
       char ch = evt.getKeyChar();
       if(ch >= 'a'  && ch <='z' ||ch>='A' && ch<='Z' ||  ch==KeyEvent.VK_BACK_SPACE ||  ch==KeyEvent.VK_DELETE)
       {
           username.setEditable(true);
       }
       else
       {
           username.setEditable(false);
       }
    }//GEN-LAST:event_usernameKeyTyped

    private void rejestracjaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rejestracjaActionPerformed
       String user = username.getText();
       String mail = email.getText();
       String pass = password.getText();
       String c_pass = c_password.getText();
       
       if(user.length() < 2 || user.length() > 20 || !(Pattern.matches("^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$", mail)) || pass.length() <4 || !c_pass.equals(pass))
       {
       if(user.length() < 2)
       {
           user_blad.setText("Minimum 2 znaki");
       }
       else if(user.length() > 20)
       {
           user_blad.setText("Maksymalnie 20 znakow");
       }
       else
       {
           user_blad.setText("");
       }
       if (!(Pattern.matches("^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$", mail))) 
       {
           mail_blad.setText("Blednie wprowadzono maila");
       }
       else
       {
           mail_blad.setText("");
       }
       if(pass.length() <4)
       {
           pass_blad.setText("Minimum 4 znaki");
       }
       else
       {
           pass_blad.setText("");
       }
       if(!c_pass.equals(pass))
       {
           c_blad.setText("Hasla nie sa takie same");
       }
       else
       {
            c_blad.setText("");
       }
      }
      else
       {
           try {
               FileWriter fw = new FileWriter(f,true);
               fw.append(user+":"+mail+":"+pass+"\n");
               username.setText("");
               email.setText("");
               password.setText("");
               c_password.setText("");
                fw.close();
               fw = new FileWriter(mail_plik,true);
               fw.append(mail+"\n");
                fw.close();
               fw = new FileWriter(hasla_plik,true);
               fw.append(pass+"\n");
               fw.close();
           } catch (IOException ex) {
               Logger.getLogger(rejestracja_logowanie.class.getName()).log(Level.SEVERE, null, ex);
           }
       }
    }//GEN-LAST:event_rejestracjaActionPerformed

