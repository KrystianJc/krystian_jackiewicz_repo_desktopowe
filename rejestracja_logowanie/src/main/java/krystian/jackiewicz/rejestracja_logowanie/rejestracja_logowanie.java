    private void ZalogujActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ZalogujActionPerformed
        boolean mail_jest = false,haslo_jest = false;
        String mail = log_mail.getText();
        String pass = log_password.getText();
        String user = username.getText();
        String tresc = "",hasla;
        Scanner sc;
        try {
            sc = new Scanner(mail_plik);
            
        while(sc.hasNext())
        {
            tresc = sc.nextLine();
            if(mail.contains(tresc))
            {
               mail_jest = true;
            }
        }
        
        } catch (FileNotFoundException ex) {
            Logger.getLogger(rejestracja_logowanie.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        try {
            sc = new Scanner(hasla_plik);
            while(sc.hasNext())
        {
            hasla = sc.nextLine();
            if(pass.contains(hasla))
            {
               haslo_jest = true;
            }
            
        }
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(rejestracja_logowanie.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(mail_jest && haslo_jest)
        {
            JOptionPane.showMessageDialog(null,"Witaj: "+user,"Pomyslnie zalogowano",JOptionPane.PLAIN_MESSAGE);
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Blad","Blednie wprowadzono dane",JOptionPane.PLAIN_MESSAGE);
        }
      
    }//GEN-LAST:event_ZalogujActionPerformed

